#include "PowerManager.h"
#include <avr/io.h>

void PowerManager::sleep()
{
    ADCSRA  = 0;
    ACSR    = 1 << 7;
    SMCR    = 5;
    asm("sleep");
}

void PowerManager::deepSleep()
{
    MCUCR = 0b01100000;
    MCUCR = 0b01000000;
    PowerManager::sleep();
}
